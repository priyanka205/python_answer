# 2) Write a Python program that will return true if the two given integer values
#  are equal or their sum or difference is 5.

num_x = int(input("Enter a number x : "))
num_y = int(input("Enter a number y : "))

if num_x == num_y:
    print (True)

if num_x + num_y == 5:
    print (True)

if num_x - num_y == 5:
    print (True)

if num_y - num_x == 5:
    print (True)



