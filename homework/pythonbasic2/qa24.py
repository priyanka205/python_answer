# 24) Calculate mutiplication of two random float numbers
# Note:

# First random float number must be between 0.1 and 1

# Second random float number must be between 9.5 and 99.5

import random

my_fname1  = random.uniform(0.1, 1)
print(my_fname1)
my_fname2 = random.uniform(9.5, 99.5)
print(my_fname2)

multi = my_fname1 * my_fname2
print(multi)